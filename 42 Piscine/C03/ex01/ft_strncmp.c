#include <stdio.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (n > 0 && (s1[i] != '\0' || s2[j] != '\0'))
	{
		if (s1[i] != s2[j])
		{
			return (s1[i] - s2[j]);
		}
		i++;
		j++;
		n--;
	}
	return (0);
}
